package dynstud;



public class Students {
    private String Name;
    private String Surname;
    private String Sex;
    private int course;
    private double midMark;
    public Students(String Name, String Surname, String Sex, int course, double midMark)
    {
        this.Name = Name;
        this.Surname = Surname;
        this.Sex = Sex;
        this.course = course;
        this.midMark = midMark;
    }
}
